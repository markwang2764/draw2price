# 执行导入
# ============================================
def import_category(category_name, category_code, items):
    print(f"\n【{category_name} {category_code}】")
    for item in items:
        try:
            knowledge_service.add_document(
                content=item["content"],
                title=item["title"],
                category=category_code
            )
            print(f"  ✓ {item['title']}")
        except Exception as e:
            print(f"  ✗ {item['title']}: {e}")

# 导入所有分类
import_category("工艺路线库", "process_route", process_routes)
import_category("设备库", "tool", tools)
import_category("成本库", "cost", costs)
import_category("特征信息库", "feature", features)

# 统计
stats = knowledge_service.get_stats()
print("\n" + "=" * 60)
print(f"  导入完成！当前知识库共 {stats['count']} 条知识")
